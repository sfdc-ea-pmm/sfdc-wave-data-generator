package pmmtech;

/**
 * HeldAwayItem
 */
public class HeldAwayItem {

    private int relativeProbability;
    private boolean heldAway;

    public int getRelativeProbability() {
        return this.relativeProbability;
    }

    public void setRelativeProbability(int relativeProbability) {
        this.relativeProbability = relativeProbability;
    }

    public boolean isHeldAway() {
        return this.heldAway;
    }

    public boolean getHeldAway() {
        return this.heldAway;
    }

    public void setHeldAway(boolean heldAway) {
        this.heldAway = heldAway;
    }

}